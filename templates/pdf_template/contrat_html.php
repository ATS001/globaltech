<p><strong><u>ET:</u></strong></p>
<p><strong><u>&nbsp;</u></strong></p>
<p><strong>La Soci&eacute;t&eacute; AFRICA TELECOM SOLUTION ATS, </strong>sis 12, Rue Sabri Boujemaa &ndash;CASABLANCA-</p>
<p>Repr&eacute;sent&eacute; par <strong>Mr Rachid KADA</strong><strong>.</strong></p>
<p><strong>&nbsp;</strong></p>
<p><strong>&nbsp;</strong></p>
<p><strong><u>Ci-apr&egrave;s d&eacute;sign&eacute; &laquo;&nbsp;G&eacute;rant&nbsp;&raquo;</u></strong></p>
<p><strong>&nbsp;<u>D&rsquo;AUTRE PART</u></strong></p>
<p><strong><u>&nbsp;</u></strong></p>
<p><strong><u>Il a &eacute;t&eacute; convenu et arr&ecirc;t&eacute; ce qui suit&nbsp;:</u></strong></p>
<p><strong><u>&nbsp;</u></strong></p>
<p><strong><u>&nbsp;</u></strong></p>
<p><strong><u>Article 1&nbsp;: Objet</u></strong></p>
<p><strong><u>&nbsp;</u></strong></p>
<p>Le pr&eacute;sent contrat a pour objet l&rsquo;emploi du Salari&eacute;<strong>, </strong>au service de la <strong>Soci&eacute;t&eacute;</strong> <strong>AFRICA TELECOM SOLUTION </strong>en qualit&eacute; de &laquo;&nbsp;Technicien &raquo;.</p>
<p><strong>&nbsp;</strong></p>
<p><strong>&nbsp;</strong></p>
<p><strong><u>Article 2&nbsp;: Description des responsabilit&eacute;s&nbsp;:</u></strong></p>
<p><strong><u>&nbsp;</u></strong></p>
<p><strong>Le Salari&eacute;</strong>, s&rsquo;engage express&eacute;ment &agrave; accepter toute modification n&eacute;cessit&eacute;e par les imp&eacute;ratifs d&rsquo;organisation de la Soci&eacute;t&eacute;.</p>
<p>&nbsp;</p>
<p>&nbsp;</p>
<p><strong><u>Article 3&nbsp;: Conditions de travail</u></strong></p>
<p><strong><u>&nbsp;</u></strong></p>
<p>Le salari&eacute; exercera ses fonctions sous l&rsquo;autorit&eacute; et dans le cadre des instructions donn&eacute;es par son sup&eacute;rieur hi&eacute;rarchique.</p>
<p>&nbsp;</p>
<p>En fonction des n&eacute;cessit&eacute;s d&rsquo;organisation du travail, l&rsquo;employeur pourra affecter le salari&eacute; aux divers postes de travail correspondant &agrave; la nature de son emploi.</p>
<p>&nbsp;</p>
<p>L&rsquo;horaire de travail est collectif en vigueur dans l&rsquo;&eacute;tablissement. Il sera susceptible d&rsquo;&ecirc;tre modifi&eacute; par l&rsquo;employeur.</p>
<p>&nbsp;</p>
<p>&nbsp;</p>
<p>&nbsp;</p>
<p><strong><u>Article 5&nbsp;: Lieu d'affectation:</u></strong></p>
<p><strong><u>&nbsp;</u></strong></p>
<p>Dans le cadre de pr&eacute;sent contrat, le salari&eacute; sera affect&eacute; en plein temps et pendant la dur&eacute;e de pr&eacute;sent contrat dans le pays <strong>Guin&eacute;e Equatorial,</strong> sous ordre de client d'AFRICA TELECOM SOLUTION sur place, et le sup&eacute;rieur hi&eacute;rarchique.</p>
<p><strong><u>&nbsp;</u></strong></p>
<p><strong><u>&nbsp;</u></strong></p>
<p><strong><u>Article 4&nbsp;: Dur&eacute;e</u></strong></p>
<p>Le pr&eacute;sent contrat qui prend effet le <strong>10/12/2014</strong> est conclu pour une dur&eacute;e de Trois&nbsp; mois &laquo;&nbsp;03&nbsp;&raquo; et prendra fin de plein droit et sans formalit&eacute;s le <strong>10/03/2015</strong>.</p>
<p>Toutefois, chacune des parties se r&eacute;serve mutuellement le droit de mettre fin au contrat imm&eacute;diatement en cas de faute grave de l&rsquo;autre partie ou de force majeure.</p>
<p>En cas ou le projet n&rsquo;est pas terminer, le pr&eacute;sent contrat sera prolonger automatiquement jusqu'&agrave; la fin du projet.</p>
<p>&nbsp;</p>
<p><strong><u>Article 5&nbsp;: P&eacute;riode d&rsquo;essai</u></strong></p>
<p>Le contrat ne deviendra d&eacute;finitif qu'&agrave; l'issue d'une p&eacute;riode d'essai de deux semaines, au cours de laquelle chacune des parties pourra rompre le contrat sans indemnit&eacute;.</p>
<p>Le salari&eacute; s&rsquo;engage &agrave; fournir pendant la p&eacute;riode d&rsquo;essai tous les &eacute;l&eacute;ments n&eacute;cessaires pour constituer son dossier.</p>
<p>&nbsp;</p>
<p><strong><u>Article 6&nbsp;: Absences</u></strong></p>
<p><strong><u>&nbsp;</u></strong></p>
<p>En cas d&rsquo;absence pour maladie ou accident, le salari&eacute; devra imm&eacute;diatement en aviser l&rsquo;employeur et en justifier le motif par la production d&rsquo;un certificat m&eacute;dical dans les 24 heures.</p>
<p>&nbsp;</p>
<p><strong><u>Article 7&nbsp;: R&eacute;mun&eacute;ration</u></strong></p>
<p><strong><u>&nbsp;</u></strong></p>
<p><strong><u>&nbsp;</u></strong></p>
<p>En contre partie de l&rsquo;accomplissement de ses fonctions, le salari&eacute; percevra un salaire mensuel Net &eacute;gal &agrave; <strong>12 000 DHS (Douze &nbsp;Milles Dirhams). </strong>Inclus salaire de base, frais de mission et prime d'expatriation dans le pays d'affectation .</p>
<p>&nbsp;</p>
<p>&nbsp;</p>
<p>L&rsquo;employeur paye tout frais relatives &agrave; la mission du salari&eacute;, en esp&egrave;ce ou par virement bancaire dans le compte du salarie selon la demande de ce dernier.</p>
<p>&nbsp;</p>
<p>le salari&eacute; peut communiquer, avant la fin du chaque mois, la fa&ccedil;on et le lieu de r&eacute;ception de ces frais de missions et prime d'expatriation.</p>
<p><strong><u>&nbsp;</u></strong></p>
<p><strong><u>&nbsp;</u></strong></p>
<p><strong><u>&nbsp;</u></strong></p>
<p><strong><u>&nbsp;</u></strong></p>
<p><strong><u>&nbsp;</u></strong></p>
<p><strong><u>Article 8&nbsp;: Conditions professionnel:</u></strong></p>
<p><strong><u>&nbsp;</u></strong></p>
<ul>
<li><strong><u>Mat&eacute;riel:</u></strong></li>
</ul>
<p><strong><u>&nbsp;</u></strong></p>
<p>Il est &agrave; la charge de l&rsquo;employeur, la fourniture des outils et mat&eacute;riels n&eacute;cessaires pour ex&eacute;cuter les taches demand&eacute;es au salari&eacute;, toute fois ce dernier est responsable d'utiliser ces outils uniquement pour le compte de ATS et veiller de limiter des dommages &agrave; ces outils de travails.</p>
<p>&nbsp;</p>
<ul>
<li><strong><u>Transport:</u></strong></li>
</ul>
<p>&nbsp;</p>
<p>L&rsquo;employeur est charg&eacute; de fournir ou payer le moyen de transport pour le salari&eacute; pour chaque d&eacute;placement entrant dans le cadre professionnel de ATS.</p>
<p>pour les d&eacute;placements hors de la royaume du Maroc ATS paye un (1) billet d'avion aller et retour vers le pays de destination&nbsp; pour chaque six mois &agrave; compter du premier jour de la mission.</p>
<ul>
<li><strong><u>Ordre de mission:</u></strong></li>
</ul>
<p>&nbsp;</p>
<p>Chaque d&eacute;placement de salari&eacute; doit &ecirc;tre notifi&eacute; par un ordre de mission sign&eacute; par le chef de projet dont la mission entre dans les limites de ces responsabilit&eacute;s.</p>
<p>&nbsp;</p>
<ul>
<li><strong><u>Assurance maladie et vaccination :</u></strong></li>
</ul>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</p>
<p>L&rsquo;employeur doit souscrire le salari&eacute; dans une assurance professionnel, et elle doit veillez que&nbsp; cette souscription est valable toute au long de la validit&eacute; du pr&eacute;sent contrat.</p>
<p>&nbsp;</p>
<p>Le salari&eacute; devrait effectuer tout type des test et &nbsp;vaccination contre les maladies &nbsp;fr&eacute;quentes dans le pays d'expatriation dans le centre pasteur Casablanca avant le voyage du salari&eacute;. les frais des vaccinations sont &agrave; la charge d'employeur.&nbsp;</p>
<p>&nbsp;</p>
<p>&nbsp;</p>
<p><strong><u>&nbsp;</u></strong></p>
<p>&nbsp;</p>
<p><strong><u>Article 9&nbsp;: Secret professionnel.</u></strong></p>
<p><strong><u>&nbsp;</u></strong></p>
<p>Le salari&eacute; s&rsquo;oblige &agrave; observer le secret professionnel absolu sur les informations dont il a fait connaissance ou de se livrer &agrave; un quelconque acte de concurrence d&eacute;loyale.</p>
<p>&nbsp;</p>
<p>&nbsp;</p>
<p>&nbsp;</p>
<p><strong><u>Article 10&nbsp;: Comp&eacute;tence juridictionnelle.</u></strong></p>
<p><strong><u>&nbsp;</u></strong></p>
<p>Tous litiges pouvant survenir &agrave; l&rsquo;occasion de l&rsquo;ex&eacute;cution du pr&eacute;sent contrat, seront soumis &agrave; la comp&eacute;tence de l&rsquo;un des quelconques tribunaux de Casablanca, au choix exclusif de l&rsquo;employeur.</p>
<p>&nbsp;</p>
<p>&nbsp;</p>
<p>&nbsp;</p>
<p>&nbsp;</p>
<p>&nbsp;</p>
<p>&nbsp;</p>
<p>&nbsp;</p>
<p><strong><u>Article 11&nbsp;: Election de domicile</u></strong></p>
<p><strong><u>&nbsp;</u></strong></p>
<p>Les parties d&eacute;clarent &eacute;lire domicile chacune &agrave; son adresse respective, telle que vis&eacute;e ci-dessus.</p>
<p>&nbsp;</p>
<p>Fait &agrave; &nbsp; Casablanca le <strong>04/12/2014</strong></p>
